// ============================================================
//  坦克大战 — 地图生成 (32×24)
// ============================================================

import type { GameMap } from './types';
import { CellType } from './types';
import { COLS, ROWS } from './constants';

export function createMap(): GameMap {
  const m: GameMap = Array.from({ length: ROWS }, () =>
    Array(COLS).fill(CellType.EMPTY)
  );

  // ====== 砖墙（可破坏） ======
  // 在开放区域散布墙体，形成掩体结构
  const brickPatterns: [number, number][][] = [
    // 左上角区域 blocks (col 2-8, row 2-18)
    [[2, 2], [2, 3]], [[2, 6], [2, 7]],
    [[4, 4], [4, 5]], [[4, 8], [4, 9]],
    [[6, 2], [6, 3]], [[6, 6], [6, 7]],
    [[8, 4], [8, 5]], [[8, 8], [8, 9]],
    [[10, 2], [10, 3]], [[10, 6], [10, 7]],
    [[12, 4], [12, 5]],
    [[14, 3], [14, 4]], [[14, 7], [14, 8]],
    [[16, 2], [16, 3]],
    [[18, 5], [18, 6]],

    // 右上角区域 (col 22-29)
    [[2, 23], [2, 24]], [[2, 27], [2, 28]],
    [[4, 21], [4, 22]], [[4, 25], [4, 26]],
    [[6, 23], [6, 24]], [[6, 27], [6, 28]],
    [[8, 21], [8, 22]], [[8, 25], [8, 26]],
    [[10, 23], [10, 24]], [[10, 27], [10, 28]],
    [[12, 25], [12, 26]],
    [[14, 22], [14, 23]], [[14, 26], [14, 27]],
    [[16, 25], [16, 26]],
    [[18, 23], [18, 24]],

    // 中间区域 blocks (col 13-18)
    [[5, 14], [5, 15]],
    [[7, 13], [7, 14]], [[7, 16], [7, 17]],
    [[9, 14], [9, 15]],
    [[11, 13], [11, 14]], [[11, 16], [11, 17]],
    [[13, 14], [13, 15]],
    [[15, 13], [15, 14]], [[15, 16], [15, 17]],

    // 下半区零散掩体
    [[19, 5], [19, 6]], [[19, 10], [19, 11]], [[19, 19], [19, 20]], [[19, 24], [19, 25]],
    [[20, 3], [20, 4]], [[20, 8], [20, 9]], [[20, 14], [20, 15]], [[20, 21], [20, 22]], [[20, 26], [20, 27]],
  ];

  for (const block of brickPatterns) {
    for (const [r, c] of block) {
      if (r >= 0 && r < ROWS && c >= 0 && c < COLS) {
        m[r][c] = CellType.BRICK;
      }
    }
  }

  // ====== 钢墙（不可破坏） ======
  const steelPositions: [number, number][] = [
    [3, 1], [3, 2], [3, 28], [3, 29],
    [7, 6], [7, 7], [7, 23], [7, 24],
    [11, 3], [11, 4], [11, 26], [11, 27],
    [15, 8], [15, 9], [15, 21], [15, 22],
    [18, 1], [18, 2], [18, 28], [18, 29],
    [21, 6], [21, 7], [21, 23], [21, 24],
    [22, 3], [22, 4], [22, 26], [22, 27],
  ];
  for (const [r, c] of steelPositions) {
    if (r >= 0 && r < ROWS && c >= 0 && c < COLS) {
      m[r][c] = CellType.STEEL;
    }
  }

  // ====== 清空玩家出生区域 ======
  for (let r = Math.max(0, ROWS - 4); r < ROWS; r++) {
    for (let c = Math.max(0, Math.floor(COLS / 2) - 4); c < Math.min(COLS, Math.floor(COLS / 2) + 4); c++) {
      m[r][c] = CellType.EMPTY;
    }
  }

  // ====== 清空地图边缘（确保出生点可用） ======
  // 上边缘 row 0-1
  for (let c = 0; c < COLS; c++) {
    m[0][c] = CellType.EMPTY;
    m[1][c] = CellType.EMPTY;
  }
  // 下边缘 rows 22-23
  for (let c = 0; c < COLS; c++) {
    m[ROWS - 2][c] = CellType.EMPTY;
    m[ROWS - 1][c] = CellType.EMPTY;
  }
  // 左边缘 col 0-1
  for (let r = 0; r < ROWS; r++) {
    m[r][0] = CellType.EMPTY;
    m[r][1] = CellType.EMPTY;
  }
  // 右边缘 col 30-31
  for (let r = 0; r < ROWS; r++) {
    m[r][COLS - 2] = CellType.EMPTY;
    m[r][COLS - 1] = CellType.EMPTY;
  }

  return m;
}
